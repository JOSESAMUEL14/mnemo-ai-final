/* =====================================================================
   Mnemo AI — PDF Export Utility
   Requires jsPDF (loaded via CDN before this file — see HTML wiring
   instructions in the accompanying message).
   ===================================================================== */

/**
 * Exports an array of journal entries as a downloadable PDF "life story".
 *
 * @param {string} title - Title shown on the cover page (e.g. "My Life Story")
 * @param {Array<{date: string, mood: string, text: string}>} entries
 *        Same shape as journal.html's localStorage data:
 *        { date: "2026-06-30", mood: "Happy", text: "Today I..." }
 */
function exportMemoriesAsPDF(title, entries) {
  // ---- Guard: handle missing/empty entries gracefully ----
  if (!entries || !Array.isArray(entries) || entries.length === 0) {
    alert('No memories to export yet');
    return;
  }

  // ---- Guard: make sure jsPDF actually loaded ----
  if (typeof window.jspdf === 'undefined') {
    alert('PDF export is unavailable right now. Please refresh and try again.');
    return;
  }

  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({ unit: 'pt', format: 'a4' });

  const pageWidth  = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const margin      = 56;
  const contentWidth = pageWidth - margin * 2;

  // Mood -> emoji map (mirrors journal.html / timeline.html)
  const moodEmojiMap = {
    Happy: '😊', Determined: '😤', Sad: '😔', Anxious: '😰',
    Tired: '😴', Energized: '🔥', Frustrated: '😤', Growing: '🌱',
    Unspecified: '📝'
  };

  // ---- Helper: adds the footer to whichever page is currently active ----
  function addFooter() {
    doc.setFont('helvetica', 'italic');
    doc.setFontSize(9);
    doc.setTextColor(150, 150, 150);
    doc.text(
      'Generated by Mnemo AI',
      pageWidth / 2,
      pageHeight - 28,
      { align: 'center' }
    );
  }

  // ---- Helper: nicely formats an ISO date string ----
  function formatDate(isoStr) {
    const d = new Date(isoStr);
    if (isNaN(d.getTime())) return isoStr; // fallback if not parseable
    const days = ['Sunday','Monday','Tuesday','Wednesday','Thursday','Friday','Saturday'];
    const months = ['January','February','March','April','May','June','July',
                     'August','September','October','November','December'];
    return `${days[d.getDay()]}, ${d.getDate()} ${months[d.getMonth()]} ${d.getFullYear()}`;
  }

  /* =====================================================================
     COVER PAGE
     ===================================================================== */
  doc.setFillColor(7, 7, 26); // Mnemo dark background tone
  doc.rect(0, 0, pageWidth, pageHeight, 'F');

  doc.setTextColor(167, 139, 250); // purple-light accent
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(16);
  doc.text('MNEMO AI', pageWidth / 2, pageHeight / 2 - 60, { align: 'center' });

  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(30);
  const safeTitle = title || 'My Life Story';
  doc.text(safeTitle, pageWidth / 2, pageHeight / 2 - 20, { align: 'center' });

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(11);
  doc.setTextColor(180, 180, 200);
  doc.text(
    `${entries.length} memor${entries.length === 1 ? 'y' : 'ies'} · Generated ${formatDate(new Date().toISOString())}`,
    pageWidth / 2,
    pageHeight / 2 + 14,
    { align: 'center' }
  );

  addFooter();

  /* =====================================================================
     ONE PAGE SECTION PER ENTRY
     Sorted oldest -> newest so the story reads chronologically.
     ===================================================================== */
  const sortedEntries = entries.slice().sort((a, b) => new Date(a.date) - new Date(b.date));

  sortedEntries.forEach((entry) => {
    doc.addPage();

    // White background for readable body pages
    doc.setFillColor(255, 255, 255);
    doc.rect(0, 0, pageWidth, pageHeight, 'F');

    let cursorY = margin;

    // ---- Date header ----
    doc.setTextColor(124, 58, 237); // purple
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(16);
    doc.text(formatDate(entry.date), margin, cursorY);
    cursorY += 26;

    // ---- Mood line (emoji + label) ----
    const moodLabel = entry.mood || 'Unspecified';
    const emoji = moodEmojiMap[moodLabel] || '📝';
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(12);
    doc.setTextColor(90, 90, 90);
    doc.text(`${emoji}  ${moodLabel}`, margin, cursorY);
    cursorY += 24;

    // ---- Divider line ----
    doc.setDrawColor(220, 220, 230);
    doc.setLineWidth(0.5);
    doc.line(margin, cursorY, pageWidth - margin, cursorY);
    cursorY += 24;

    // ---- Full journal text, properly word-wrapped ----
    const bodyText = entry.text || '(No content written for this entry)';
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(12);
    doc.setTextColor(30, 30, 30);

    const lines = doc.splitTextToSize(bodyText, contentWidth);
    const lineHeight = 18;
    const usableBottom = pageHeight - margin - 30; // leave room for footer

    lines.forEach((line) => {
      // If a single entry overflows one page, spill onto a new page
      if (cursorY + lineHeight > usableBottom) {
        addFooter();
        doc.addPage();
        doc.setFillColor(255, 255, 255);
        doc.rect(0, 0, pageWidth, pageHeight, 'F');
        cursorY = margin;
        doc.setFont('helvetica', 'normal');
        doc.setFontSize(12);
        doc.setTextColor(30, 30, 30);
      }
      doc.text(line, margin, cursorY);
      cursorY += lineHeight;
    });

    addFooter();
  });

  // ---- Trigger download ----
  doc.save('mnemo-life-story.pdf');
}